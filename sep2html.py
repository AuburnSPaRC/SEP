RFCURL = 'http://www.faqs.org/rfcs/rfc%d.html'
SEPURL = 'sep-%04d.html'
SEPSVNURL = ('https://github.com/AuburnSPaRC/SEP/blob/gh-pages/sep-%04d.html')
SEPDIRRUL = 'http://AuburnSPaRC.github.com/SEP/'

